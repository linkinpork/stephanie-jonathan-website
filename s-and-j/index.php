                                <p>Eric and I first met at church during Chinese school on Friday nights. Our parents tried to orchestrate events so that we could become friends, but those never seemed to pan out. It wasn&#39;t until we started maturing in our relationship with Jesus that our acquaintance developed into friendship. Not everything was smooth-sailing from there though. In fact, I remember butting heads quite often arguing about whatever (nerdy) video game we were playing at the time. While we&#39;ve had our differences, it was his love for God and desire to obey Him (along with his goofiness and quirkiness) that drew me to him and led me to greatly respect him. My fondest memories with Eric are summer nights spent lounging around at his home, talking and praying for each other. Eric is recently married to Kathleen, loves serving the youth, and enjoys tinkering and home improvement projects. </p>
                                <h4>Groomsman: Romin Bahk</h4>
                                <img src="/img/weddingparty/romin.jpg" height="230" width="230" align="left" style="margin-right:30px;" />
                                <p>I owe it Romin for putting up with countless hours of chick flicks and Disney movies (though he was partially compensated with baked goods). He&#39;s been there from the start of my relationship and earned the title of &quot;official third wheel.&quot; My friendship with Romin extends back to high school where we took several classes together. When we came to UCLA, we got to know each other a lot better because I was one of the few people he already knew. My fondest memories with Romin come from living together--from late-night runs to Monterey Park for Chinese food to playing XBOX with our roommates, it&#39;s safe to say we never got any real work done. Romin is one of the most passionate and enthusiastic (talkative) guys that I know. You know that Romin won&#39;t be afraid to speak the truth, even if it&#39;s something you don&#39;t want to hear, but he also won&#39;t hesitate to stick up and defend you if he senses that you&#39;ve been wronged. Romin is the director of a tutoring/after-school center and pursuing pastoral ministry. </p>
                                <h4 style="text-align:right">Groomsman: James Yang</h4>
                                <img src="/img/weddingparty/james.jpg" height="225" width="225" align="right" style="margin-left:30px;" />
                                <p>James and I were in the same small group our freshman year at UCLA. As with many forced friendships, things started awkwardly. One of the first things I learned about him was that he was an avid aquascaper--I had no clue what that was until meeting him. Aquascaping aside, I also noticed a deep desire to know God more. Seeing this, I knew I wanted to be life-long friends. When I share with James he&#39;s easily able to identify and empathize with me and always seems to have a timely word of encouragement or exhortation for me. I know that God has blessed me greatly through James. He&#39;s the kind of person who&#39;s able to genuinely share in your joys and sorrow. James is currently pursuing a master&#39;s degree in civil/geo-engineering while working part time at a geotech firm. He is heavily involved with his church and is an ardent Dodgers fan. </p>
                                <h4>Groomsman: Henry So</h4>
