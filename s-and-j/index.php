                                <h4>Why Marriage? </h4>
                            </div>
                            <p class="ourstory-para text-center"> Stephanie and I believe that marriage is from God and thus, it is ultimately for God. In other words, marriage is something God made and, although there are many blessings in it, it is primarily about God and not us. He created marriage to show the world an example of Jesus' love and commitment to His people (the church). By extending the love that we have experienced from Jesus to our spouse, we make a good name for God. Even though we're imperfect people, it's through the imperfections that we show how much Jesus loves us with all our imperfections and failures. 
                            </p>
                            <br>
                            <p class="ourstory-para text-center" style="font-style:italic"> "...We must say to ourselves something like this: 'Well, when Jesus looked down from the cross, he didn't think "I am giving myself to you because you are so attractive to me." No, he was in agony, and he looked down at us - denying him, abandoning him, and betraying him - and in the greatest act of love in history, he STAYED. He said, "Father, forgive them, they don't know what they are doing." He loved us, not because we were lovely to him, but to make us lovely. That is why I am going to love my spouse.' Speak to your heart like that, and then fulfill the promises you made on your wedding day."
                                <br>
                                -Timothy Keller, The Meaning of Marriage: Finding Happiness in Your Most Profound Relationship
                            </p>
