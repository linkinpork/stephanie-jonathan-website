                                <h4>The Proposal </h4>
                            </div>
                            <p class="ourstory-para text-center"> Stephanie had made plans for a double-date dinner and board games on Saturday, April 14, 2012. Seeing this as the perfect opportunity, Jonathan used this hangout as a pretense for the proposal. Through her friend, Jonathan picked a nice restaurant in Malibu for dinner. Stephanie looked up the restaurant on Yelp and saw that it was a $$$ restaurant. Not wanting to go broke from dinner and secretly not wanting to go all the way to Malibu, Stephanie asked if they could go to a different restaurant. Jonathan had to play it cool and agreed with Stephanie&#39;s preference. Secretly though, Jonathan frantically emailed her friend to relay that Stephanie wanted a more economical choice. Instead of choosing a closer restaurant, her friend picked another less expensive restaurant in Malibu&#46;&#46;&#46;so a beach in Malibu for the proposal it was.
                                <br>
                                As usual, Stephanie had work that Saturday until 4:30, after which she met Jonathan to drive to Malibu together. Jonathan had no idea how he was going to get her to the beach without her noticing. To his relief and with some divine intervention, Stephanie passed out on the car ride to the &quot;restaurant.&quot; Little did she know that they had actually passed the restaurant on their way to the beach.<br>
                                <img src="/img/proposal/Proposal1.jpg" height="160" width="240" align="left"/>
                                As they pulled into the parking lot at the beach, Stephanie groggily asked where they were and Jonathan replied, &quot;That&#39;s weird, this is the address to the restaurant.&quot; Still in the process of waking up, Stephanie was confused but got out of the car and followed him down the path to the beach. &quot;I think it&#39;s somewhere down here&quot;&#46;&#46;&#46;&quot;Um&#46;&#46;&#46;are you sure it would be down here? ON the beach?&quot;
                                <br>
                                <img src="/img/proposal/Proposal2.jpg" height="160" width="240" align="right"/>
                                Stephanie&#39;s mind began to race thinking of the possibilities. A few steps later, Jonathan stopped abruptly and asked, &quot;What&#39;s that?&quot; pointing to something on the ground. Confused, Stephanie bent down to pick up the piece of paper and saw a drawing of a polar bear and walrus. Still puzzled, Stephanie looked up and saw that a trail of drawings leading up to a picnic blanket/basket. At that point, Stephanie knew exactly what was happening.  Each drawing  illustrated the story of their relationship and was from the book that Stephanie would eventually find at the end of the trail.
                                <br>
                                <img src="/img/proposal/Proposal3.jpg" height="160" width="240" align="left"/>
                                After speedily reading the book together, Jonathan reached into the picnic basket and pulled out a box. Her response was: &quot;You left that here by itself?!&quot; Jonathan then said some really nice things (that Stephanie cannot recall) and proposed! After the ring went on the finger, their friends started popping out from nowhere (at least to Stephanie). It turns out Jonathan didn&#39;t leave the ring by itself.
                            </p>
