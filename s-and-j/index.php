                                <p>I got to know Henry my first year at UCLA in an Asian American studies class. By a strange series of events, we ended up dorming together our second year. One of the most unique things about Henry is that he is the most logical person I know. Because he thinks so differently from me, he&#39;s given me a different and valuable perspective in many of the big decisions I&#39;ve had to make. While he may be logical, he is no robot. He&#39;s prayed with me through the ups and downs when Stephanie and I were dating. I&#39;ve had the privilege of being one of his groomsmen at his wedding and, I guess, this is his turn. Henry is married to Jung Hwa, enjoys good food and Bruins football. </p>
